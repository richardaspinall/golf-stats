import * as fs from 'node:fs/promises';
import * as path from 'node:path';

const backendRoot = path.resolve(process.cwd());
const contractsRoot = path.join(backendRoot, 'contracts');
const srcRoot = path.join(backendRoot, 'src');

const schemaImports = new Set([
  'arraySchema',
  'booleanSchema',
  'literalSchema',
  'noPayloadSchema',
  'numberSchema',
  'objectSchema',
  'optionalSchema',
  'nullableSchema',
  'stringSchema',
  'unknownSchema',
]);

const refTypeMap = {
  Round: { typeImport: 'Round', schemaImport: 'roundSchema' },
  RoundSummary: { typeImport: 'RoundSummary', schemaImport: 'roundSummarySchema' },
  RoundListItem: { typeImport: 'RoundListItem', schemaImport: 'roundListItemSchema' },
};

const autoGeneratedHeader = `// AUTO-GENERATED FILE. DO NOT EDIT.\n`;

const unique = (values) => [...new Set(values)];

const toSchemaExpression = (property) => {
  const { type, required, nullable } = property;
  const base = (() => {
    if (type.endsWith('[]')) {
      const inner = { ...property, type: type.slice(0, -2), required: true, nullable: false };
      return `arraySchema(${toSchemaExpression(inner)})`;
    }

    if (type === 'string') return 'stringSchema()';
    if (type === 'number') return 'numberSchema()';
    if (type === 'boolean') return 'booleanSchema()';
    if (type === 'unknown') return 'unknownSchema()';

    const ref = refTypeMap[type];
    if (!ref) throw new Error(`Unsupported property type: ${type}`);
    return ref.schemaImport;
  })();

  const withNullability = nullable ? `nullableSchema(${base})` : base;
  return required ? withNullability : `optionalSchema(${withNullability})`;
};

const toTypeExpression = (property) => {
  const { type, required, nullable } = property;
  const base = (() => {
    if (type.endsWith('[]')) {
      const inner = { ...property, type: type.slice(0, -2), required: true, nullable: false };
      return `${toTypeExpression(inner)}[]`;
    }

    if (type === 'string' || type === 'number' || type === 'boolean' || type === 'unknown') {
      return type;
    }

    const ref = refTypeMap[type];
    if (!ref) throw new Error(`Unsupported property type: ${type}`);
    return ref.typeImport;
  })();

  const withNullability = nullable ? `${base} | null` : base;
  return required ? withNullability : `${withNullability} | undefined`;
};

const buildTypesFile = (contract) => {
  const refImports = unique(
    [...contract.payload.properties, ...contract.output.properties]
      .map((property) => (property.type.endsWith('[]') ? property.type.slice(0, -2) : property.type))
      .filter((type) => refTypeMap[type])
      .map((type) => refTypeMap[type].typeImport),
  );

  const importLine = refImports.length
    ? `import type { ${refImports.join(', ')} } from '../../../../domain/types.js';\n\n`
    : '';

  const payloadBody = contract.payload.properties
    .map((property) => `  ${property.name}${property.required ? '' : '?'}: ${toTypeExpression(property)};`)
    .join('\n');
  const outputBody = contract.output.properties
    .map((property) => `  ${property.name}${property.required ? '' : '?'}: ${toTypeExpression(property)};`)
    .join('\n');
  const errorCodesBody =
    contract.errors.length > 0
      ? contract.errors.map((error) => `  ${error.errorCode}: '${error.errorCode}',`).join('\n')
      : '';

  return `${autoGeneratedHeader}${importLine}type ${contract.handlerName}Payload = {\n${payloadBody}\n};\n\n` +
    `type ${contract.handlerName}Output = {\n${outputBody}\n};\n\n` +
    `const ${contract.handlerName}ErrorCodes = {\n${errorCodesBody}\n} as const;\n\n` +
    `type ${contract.handlerName}ErrorCode = typeof ${contract.handlerName}ErrorCodes[keyof typeof ${contract.handlerName}ErrorCodes];\n\n` +
    `export type { ${contract.handlerName}Payload, ${contract.handlerName}Output, ${contract.handlerName}ErrorCode };\n` +
    `export { ${contract.handlerName}ErrorCodes };\n`;
};

const buildSchemasFile = (contract) => {
  const refImports = unique(
    [...contract.payload.properties, ...contract.output.properties]
      .map((property) => (property.type.endsWith('[]') ? property.type.slice(0, -2) : property.type))
      .filter((type) => refTypeMap[type])
      .map((type) => refTypeMap[type].schemaImport),
  );

  const helperImports = unique(
    [...contract.payload.properties, ...contract.output.properties].flatMap((property) => {
      const imports = ['objectSchema'];
      const rawType = property.type.endsWith('[]') ? property.type.slice(0, -2) : property.type;
      if (property.type.endsWith('[]')) imports.push('arraySchema');
      if (rawType === 'string') imports.push('stringSchema');
      if (rawType === 'number') imports.push('numberSchema');
      if (rawType === 'boolean') imports.push('booleanSchema');
      if (rawType === 'unknown') imports.push('unknownSchema');
      if (!property.required) imports.push('optionalSchema');
      if (property.nullable) imports.push('nullableSchema');
      return imports;
    }),
  ).filter((value) => schemaImports.has(value));

  const hasEmptyPayload = contract.payload.properties.length === 0;
  const payloadImportList = hasEmptyPayload ? unique([...helperImports, 'noPayloadSchema']) : helperImports;
  const refImportLine = refImports.length ? `, ${refImports.join(', ')}` : '';

  const payloadSchema =
    contract.payload.properties.length === 0
      ? `const ${contract.handlerName}PayloadSchema = noPayloadSchema;\n`
      : `const ${contract.handlerName}PayloadSchema = objectSchema({\n${contract.payload.properties
          .map((property) => `  ${property.name}: ${toSchemaExpression(property)},`)
          .join('\n')}\n});\n`;
  const outputSchema = `const ${contract.handlerName}OutputSchema = objectSchema({\n${contract.output.properties
    .map((property) => `  ${property.name}: ${toSchemaExpression(property)},`)
    .join('\n')}\n});\n`;

  return `${autoGeneratedHeader}import { ${payloadImportList.join(', ')}${refImportLine} } from '../../../../app/http/contracts.js';\n\n` +
    `${payloadSchema}\n${outputSchema}\nexport { ${contract.handlerName}PayloadSchema, ${contract.handlerName}OutputSchema };\n`;
};

const buildErrorsFile = (contract) => {
  const errors = contract.errors
    .map(
      (error) =>
        `const ${error.errorName} = {\n  errorName: '${error.errorName}',\n  errorCode: '${error.errorCode}',\n  message: '${error.message}',\n} as const;`,
    )
    .join('\n\n');
  const exportNames = contract.errors.map((error) => error.errorName).join(', ');
  return `${autoGeneratedHeader}${errors}\n\nexport { ${exportNames} };\n`;
};

const buildAbstractHandlerFile = (contract) => {
  return `${autoGeneratedHeader}import { BaseHandler } from '../../../../app/http/BaseHandler.js';\n` +
    `import { ${contract.handlerName}PayloadSchema, ${contract.handlerName}OutputSchema } from '../../../../api/gen/${contract.domainName}/schemas/${contract.handlerName}Schemas.js';\n` +
    `import type { ${contract.handlerName}Output, ${contract.handlerName}Payload } from '../../../../api/gen/${contract.domainName}/types/${contract.handlerName}.js';\n\n` +
    `export abstract class Abstract${contract.handlerName}Handler extends BaseHandler<${contract.handlerName}Payload, ${contract.handlerName}Output> {\n` +
    `  protected readonly payloadSchema = ${contract.handlerName}PayloadSchema;\n` +
    `  protected readonly outputSchema = ${contract.handlerName}OutputSchema;\n` +
    `}\n`;
};

const writeFile = async (filePath, content) => {
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, content, 'utf8');
};

const loadContracts = async () => {
  const domainDirs = await fs.readdir(contractsRoot);
  const contracts = [];

  for (const domainDir of domainDirs) {
    const fullDir = path.join(contractsRoot, domainDir);
    const stat = await fs.stat(fullDir);
    if (!stat.isDirectory()) continue;

    const files = (await fs.readdir(fullDir)).filter((file) => file.endsWith('.json'));
    for (const file of files) {
      const raw = await fs.readFile(path.join(fullDir, file), 'utf8');
      contracts.push(JSON.parse(raw));
    }
  }

  return contracts;
};

const main = async () => {
  const contracts = await loadContracts();

  for (const contract of contracts) {
    const apiGenBase = path.join(srcRoot, 'api', 'gen', contract.domainName);
    await writeFile(path.join(apiGenBase, 'types', `${contract.handlerName}.ts`), buildTypesFile(contract));
    await writeFile(path.join(apiGenBase, 'schemas', `${contract.handlerName}Schemas.ts`), buildSchemasFile(contract));
    await writeFile(path.join(apiGenBase, 'errors', `${contract.handlerName}Errors.ts`), buildErrorsFile(contract));
    await writeFile(
      path.join(srcRoot, 'features', contract.domainName, 'handlers', 'gen', `Abstract${contract.handlerName}Handler.ts`),
      buildAbstractHandlerFile(contract),
    );
  }
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
